package com.dgmf.subject3datatypeconversion;

public class DatatypeConversion {

	public static void main(String[] args) {
		/* Implicit Datatype Conversion (Casting) */
		// Store a smaller range variable into a 
		// larger range variable
		int a = 100;
		System.out.println("Int representation : " + a);
		
		long b = a;
		System.out.println("Long representation : " + b);
		
		float c = b;
		System.out.println("Float representation : " + c);
		
		

	}

}
